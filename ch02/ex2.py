# 키보드 입력

# 기본적으로는 문자열로 받기때문에 정수형으로 변환
a = int(input("첫번째 숫자를 입력하세요 : "))
b = int(input("두번째 숫자를 입력하세요 : "))
result = a + b
print(a, " + ", b, " = ", result)
result = a - b
print(a, ' - ', b, ' = ', result)
result = a * b
print(a, " * ", b, " = ", result)
result = a / b
print(a, " / ", b, " = ", result)